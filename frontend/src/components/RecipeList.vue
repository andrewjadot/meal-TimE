<template>
  <div class="recipe-list">
    <div v-for="recipe in recipes" v-bind:key="recipe.id" class="recipe">
      <router-link v-bind:to="{ name: 'Recipes', params: { id: recipe.id } }">
        {{ recipe.name }}
      </router-link>
    </div>
  </div>
</template>

<script>
import recipeService from '../services/RecipeService';

export default {
  name: 'recipe-list',
  data() {
    return {
      recipes: []
    }
  },
  created() {
    recipeService.getAllUserRecipes().then(response => {
      this.recipes = response.data;
    });
  }
}
</script>